namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;

class SecurityAlertNotification extends Notification implements ShouldQueue
{
    use Queueable;

    public $ip;
    public $userAgent;

    public function __construct($ip, $userAgent)
    {
        $this->ip = $ip;
        $this->userAgent = $userAgent;
    }

    public function via($notifiable)
    {
        return ['mail'];
    }

    public function toMail($notifiable)
    {
        return (new MailMessage)
            ->subject('Security Alert: Unauthorized Access Attempt')
            ->line('A connection attempt was blocked by the IP Whitelist policy.')
            ->line("IP Address: {$this->ip}")
            ->line("User Agent: {$this->userAgent}")
            ->line('If this was you, please update your IP Whitelist.')
            ->action('Review Security Settings', url('/settings/security'));
    }
}
