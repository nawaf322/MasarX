namespace App\Enums;

enum PaymentStatus: string
{
    case UNPAID = 'unpaid';
    case PAID = 'paid';
    case PARTIAL = 'partial';
    case REFUNDED = 'refunded';
    case COD = 'cod';

    public function label(): string
    {
        return match ($this) {
            self::UNPAID => 'Unpaid',
            self::PAID => 'Paid',
            self::PARTIAL => 'Partial',
            self::REFUNDED => 'Refunded',
            self::COD => 'Cash on Delivery',
        };
    }
}
